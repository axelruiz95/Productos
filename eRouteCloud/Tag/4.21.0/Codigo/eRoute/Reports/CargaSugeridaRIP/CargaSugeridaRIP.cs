    {
        if (FechaInicial != null)
        {
            DateTime init = DateTime.Parse(FechaInicial);
            switch (dateStatus)
            {
                case "Igual":
                    this.FechaInicial = init.Date.ToString("yyyy-MM-ddTHH:mm:ss");
                    this.FechaFinal = init.Date.ToString("yyyy-MM-ddT23:59:59");
